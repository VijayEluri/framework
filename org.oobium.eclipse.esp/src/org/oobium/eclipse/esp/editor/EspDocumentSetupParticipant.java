package org.oobium.eclipse.esp.editor;

import org.eclipse.core.filebuffers.IDocumentSetupParticipant;
import org.eclipse.jface.text.IDocument;
import org.eclipse.jface.text.IDocumentExtension3;
import org.eclipse.jface.text.IDocumentPartitioner;
import org.eclipse.jface.text.rules.FastPartitioner;
import org.eclipse.jface.text.rules.RuleBasedPartitionScanner;
import org.oobium.eclipse.esp.EspPlugin;

public class EspDocumentSetupParticipant implements IDocumentSetupParticipant {
	
	public void setup(IDocument document) {
		if (document instanceof IDocumentExtension3) {
			IDocumentExtension3 extension3 = (IDocumentExtension3) document;
			IDocumentPartitioner partitioner = new FastPartitioner(
					new RuleBasedPartitionScanner(), new String[] { IDocument.DEFAULT_CONTENT_TYPE });
			extension3.setDocumentPartitioner(EspPlugin.ESP_PARTITIONING, partitioner);
			partitioner.connect(document);
		}
	}
	
}
